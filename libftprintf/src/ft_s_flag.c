#include "ft_printf.h"

int	ft_s_flag(char *s)
{
	int	a;

	a = ft_strlen(s);
	if (s == NULL)
	{
		write (1, "(null)", 6);
		return (6);
	}
	write (1, s, a);
	return (a);
}
